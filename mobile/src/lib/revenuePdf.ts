import * as Print from "expo-print";
import * as Sharing from "expo-sharing";

export type RevenuePdfInput = {
  organizerName: string;
  asOfLabel: string;
  summary: {
    grossRevenue: number;
    platformFee: number;
    eligiblePayout: number;
    realPayments: number;
    couponPayments: number;
    freeCoupons: number;
    freeCouponFaceValue: number;
    totalPaidOut: number;
    availableBalance: number;
  };
  trips: Array<{
    tripTitle: string;
    totalBookings: number;
    grossAmount: number;
    eligibleAmount: number;
  }>;
  transactions: Array<{
    paidAt: string;
    memberName: string;
    tripTitle: string;
    grossAmount: number;
    amountPaid: number;
    type: string;
  }>;
};

function esc(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function money(n: number): string {
  return `₹${Number(n || 0).toLocaleString("en-IN", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

