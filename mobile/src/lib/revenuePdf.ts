/**
 * Generate a PDF from the given HTML, then try to share it.
 * Falls back to Print.printAsync when sharing is unavailable.
 */
export async function shareRevenuePdf(html: string): Promise<void> {
  const { uri } = await Print.printToFileAsync({
    html,
    base64: false,
  });

  try {
    const canShare = await Sharing.isAvailableAsync();
    if (canShare) {
      await Sharing.shareAsync(uri, {
        mimeType: "application/pdf",
        dialogTitle: "Trip-Sync — Export PDF",
        UTI: "com.adobe.pdf",
      });
      return;
    }
  } catch {
    // sharing threw — fall through to printAsync
  }

  // Fallback: use the system print dialog
  await Print.printAsync({ uri });
}
